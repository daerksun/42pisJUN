#include <unistd.h>

void	par(char p, char s)
{
	char r[2];

	r[0] = p;
	r[1] = s;
	r[1]++;
	while (r[0] <= '9')
	{
		while (r[1] <= '9')
		{
			write(1, &p, 1);
			write(1, &s, 1);
			write(1, " ", 1);
			write(1, &r[0], 1);
			write(1, &r[1], 1);
			if (p == '9' && s == '8' && r[0] == '9' && r[1] == '9')
				NULL;
			else
				write(1, ", ", 2);
			r[1]++;
		}
		r[1] = '0';
		r[0]++;
	}
}

void	ft_print_comb2(void)
{
	char n[2];

	n[0] = '0';
	n[1] = '0';
	while (n[0] <= '9')
	{
		while (n[1] <= '9')
		{
			par(n[0], n[1]);
			n[1]++;
		}
		n[1] = '0';
		n[0]++;
	}
}
